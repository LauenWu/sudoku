#[wasm_bindgen]
pub struct PlayfieldState {
    values: Vec<u8>,
}

#[wasm_bindgen]
impl PlayfieldState {
    pub fn new() -> PlayfieldState {
        let mut values = Vec::new();
        (0..81).for_each(|i| values.push((i % 9) + 1));
        PlayfieldState { values }
    }

    pub fn set_value(&mut self, value:u8, i:usize) {
        self.values[i] = value;
    }

    pub fn values(&self) -> *const u8 {
        self.values.as_ptr()
    }
}

